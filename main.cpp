#include <iostream>

using namespace std;

int v[10]={1,2,3,4,5,6,0};

void afisv(int p,int sf)
{
    if(p<=sf)
    {
        cout<<v[p]<<' ';
        if(p!=sf)
            return afisv(p+1,sf);
    }
}

int main()
{
    int st,sf;
    cin>>st>>sf;
    cout<<'\n';
    afisv(st,sf);
    return 0;
}

